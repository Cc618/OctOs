#pragma once

// All typedefs

namespace std
{
	// Numeric //
	typedef unsigned char		u8;
	typedef signed char			i8;
	typedef unsigned short		u16;
	typedef signed short		i16;
	typedef unsigned int		u32;
	typedef signed int			i32;

	// Memory //
	typedef u32					size_t;
	typedef size_t				sz;
	typedef u8					byte;
	typedef void* 				ptr;

	// String //
	typedef char*				str;
	typedef const char*			cstr;
} // namespace std
